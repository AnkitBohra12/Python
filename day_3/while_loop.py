# loop is a block of statements which we can execute multiple times. the block which can we include these statements
# are while or for.

# while loop
# there are 3 important things you have to remember to start while looping
# 1- initialization ( where you have to start/ starting point)
# 2- condition (when we have to stop)
# 3- incrementation (how much value we have to increment)

i = 1  # initialization
while i <= 10:  # condition
    print(i)
    i = i+1   # incrementation
print("Done!!")

# print 1....10 numbers in descending order

j = 10
while j >= 1:
    print(j)
    j = j-1
print("Done!!!")
